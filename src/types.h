#ifndef SKYLARK_TYPES_H
#define SKYLARK_TYPES_H

/* Integer types
 *
 * They aren't always going to be accurate,
 * but they'll do for this project.
 */
typedef unsigned char  uint8;
typedef char           int8;
typedef unsigned short uint16;
typedef short          int16;
typedef unsigned int   uint32;
typedef int            int32;

/* Misc types */
typedef unsigned char  bool;
#define true  1
#define false 0

#endif
